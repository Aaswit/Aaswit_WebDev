// Addition Function
function add(a, b) {   // function name(parameter)
    return a + b;
}
console.log(add) // calling add function

// Subtraction Function
function sub(a, b) {
    return a - b;
}
console.log(sub(10, 6))

// Multiplication Function
function mul(a, b) {
    return a * b;
}
console.log(mul(10, 2))

// Division Function
function div(a, b) {
    return a / b;
}
console.log(div(15, 2))

// Modulus / Remainder Function
function mod(a, b) {
    return a % b;
}
console.log(mod(77, 5))